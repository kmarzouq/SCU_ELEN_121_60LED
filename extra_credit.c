// send 32 bits out the SPI port - MSB first
void spi32(unsigned int c) {
	uint32_t f = c;
	
	
	for (int i = 0; i < 32; i++){
		HAL_GPIO_WritePin(SCK_GPIO_Port, SCK_Pin, 1);
		f = c>>(31);
		c = c<<1;
		HAL_GPIO_WritePin(SDO_GPIO_Port, SDO_Pin, f);
		HAL_GPIO_WritePin(SCK_GPIO_Port, SCK_Pin, 0);
	}
}

void send_array(){
	spi32(0);
	
	for (int i = 0; i < 60; i++){
		spi32(led.arr[i]);
	}
	
	spi32(0xFFFFFFFF);
}
